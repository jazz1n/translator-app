import re

text = """
۱۴۰۳/۰۶/۲۵
۲۵/شهریور/۱۴۰۳
۱۴۰۳-۰۶-۰۵
بیست و پنجم شهریور ماه سال ۱۴۰۳
پانزدهم دی ماه سال ۱۴۰۳
یکم مرداد ماه سال ۱۳۲۶
دو مهر ماه 1326
سوم بهمن 1337
یک اسفند سال 1335
"""


# Define the regex patterns for different date formats
patterns = [
    r'\d{4}/\d{2}/\d{2}',  # Matches 1403/06/25
    r'\d{2}/\S+/\d{4}',  # Matches 25/شهریور/1403
    r'\d{4}-\d{2}-\d{2}',  # Matches 1403-06-05
    # Matches written dates like "بیست و پنجم شهریور ماه سال ۱۴۰۳"
    r'(?:یکم|یک|دو|سوم|سه|چهارم|چهار|پنج|پانزدهم|پانزده|شانزدهم|شانزده|هفدهم|هفده|هجدهم|هجده|نوزدهم|نوزده|بیستم|بیست و یکم|بیست و یک|بیست و دوم|بیست و دو|بیست و سوم|بیست و سه|بیست و چهارم|بیست و چهار|بیست و پنجم|بیست و پنج|بیست و ششم|بیست و شش|بیست و هفتم|بیست و هفت|بیست و هشتم|بیست و هشت|بیست و نهم|بیست و نه|سی‌ام|سی) \S+ ماه(?: سال)? \d{4}',
    # Matches dates like "سوم بهمن 1337" or "دو مهر ماه 1326"
    r'(?:یکم|یک|دو|سوم|سه|چهارم|چهار|پنج|پانزدهم|پانزده|شانزدهم|شانزده|هفدهم|هفده|هجدهم|هجده|نوزدهم|نوزده|بیستم|بیست و یکم|بیست و یک|بیست و دوم|بیست و دو|بیست و سوم|بیست و سه|بیست و چهارم|بیست و چهار|بیست و پنجم|بیست و پنج|بیست و ششم|بیست و شش|بیست و هفتم|بیست و هفت|بیست و هشتم|بیست و هشت|بیست و نهم|بیست و نه|سی‌ام|سی) \S+ \d{4}',
]

# Combine the patterns into one
combined_pattern = '|'.join(patterns)

# Find all matches in the text
matches = re.findall(combined_pattern, text)

# Print results
for match in matches:
    print(match)
